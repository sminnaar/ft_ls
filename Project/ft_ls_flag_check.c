#include "ft_ls.h"

int	ft_ls_flag_check(t_flags *flag)
{
	if (flag->l != 0 || flag->R != 0 || flag->a != 0
			|| flag->r != 0 || flag->t != 0)
	{
		return (1);
	}
	else
	{
		return (0);
	}
}
